#include<cstdio>
#include<iostream>

using namespace std;

int F1[20]={1,2,4,8,16,32,64,128,256,512,1024,2048,4096,8192,16384,32768};
string F2[20]={
    "2(0)","2","2(2)","2(2+2(0))","2(2(2))","2(2(2)+2(0))","2(2(2)+2)","2(2(2)+2+2(0))","2(2(2+2(0)))","2(2(2+2(0))+2(0))","2(2(2+2(0))+2)","2(2(2+2(0))+2+2(0))","2(2(2+2(0))+2(2))","2(2(2+2(0))+2(2)+2(0))","2(2(2+2(0))+2(2)+2)"
};

int n,cur=14;

int main()
{
    cin>>n;
    while(n>0)
    {
        for(int i=cur;i>=0;i--)
        {
            if(n>=F1[i])
            {
                n-=F1[i];
                cur=i;
                if(n!=0)cout<<F2[i]<<"+";
                else cout<<F2[i]<<endl;
                break;
            }
        }
    }
    return 0;
}
